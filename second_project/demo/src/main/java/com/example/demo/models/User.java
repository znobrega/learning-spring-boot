package com.example.demo.models;

import org.springframework.stereotype.Component;

import javax.persistence.Entity;


public class User {
    private String name;
    private int age;

    public User(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
